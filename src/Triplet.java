
public class Triplet <C>{

   private C first;
   private C second;

   public C getFirst() {
      return first;
   }

   public void setFirst(C first) {
      this.first = first;
   }

   public C getSecond() {
      return second;
   }

   public void setSecond(C second) {
      this.second = second;
   }

   public C getThird() {
      return third;
   }

   public void setThird(C third) {
      this.third = third;
   }

   private C third;

   public Triplet(C first, C second, C third) {
   }
   
}

